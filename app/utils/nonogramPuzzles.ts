import type { NonogramPuzzle } from './nonogram'

// Helper to turn a string grid into a boolean[][] solution.
// '1' = filled, '0' = empty
function grid(rows: string[]): boolean[][] {
  return rows.map((r) => r.split('').map((c) => c === '1'))
}

// Helper to auto-derive clues from solution
function clues(solution: boolean[][]): { rowClues: number[][]; colClues: number[][] } {
  const rowClues = solution.map((row) => {
    const runs: number[] = []
    let run = 0
    for (const cell of row) {
      if (cell) { run++ } else if (run > 0) { runs.push(run); run = 0 }
    }
    if (run > 0) runs.push(run)
    return runs.length > 0 ? runs : [0]
  })

  const width = solution[0]?.length ?? 0
  const colClues = Array.from({ length: width }, (_, c) => {
    const runs: number[] = []
    let run = 0
    for (const row of solution) {
      const cell = row[c] ?? false
      if (cell) { run++ } else if (run > 0) { runs.push(run); run = 0 }
    }
    if (run > 0) runs.push(run)
    return runs.length > 0 ? runs : [0]
  })

  return { rowClues, colClues }
}

function make(
  id: string,
  title: string,
  rows: string[],
): NonogramPuzzle {
  const solution = grid(rows)
  const { rowClues, colClues } = clues(solution)
  return {
    id,
    title,
    width: rows[0]?.length ?? 0,
    height: rows.length,
    rowClues,
    colClues,
    solution,
  }
}

